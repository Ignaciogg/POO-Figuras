package Modelo;

public class Punto {

		//Declaración
	    private int x = 0;
	    private int y = 0;

	    //Constructor
	    public Punto (int x, int y) {
	    	this.x = x;
		    this.y = y;
	    }

	    //Getters y Setters
	    public int getX() {return x;}

	    public void setX(int x) {this.x = x;}

	    public int getY() {return y;}

	    public void setY(int y) {this.y = y;}

}